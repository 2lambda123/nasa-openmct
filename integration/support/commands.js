Cypress.Commands.add('authRequests', () => {
    cy.request({
        method: 'GET',
        url: Cypress.config().baseUrl,
        form: true,
        body: {
            Username: Cypress.env('USERNAME'),
            Password: Cypress.env('PASSWORD')
        }
    });
});
